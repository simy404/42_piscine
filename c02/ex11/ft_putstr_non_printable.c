#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	char_to_hex(char c)
{
	char	*hex;

	hex = "0123456789abcdef";
	ft_putchar(hex[c / 16]);
	ft_putchar(hex[c % 16]);
}

void	ft_putstr_non_printable(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (str[i] > 126 || str[i] < 32)
		{
			ft_putchar('\\');
			char_to_hex(str[i]);
		}
		else
			ft_putchar(str[i]);
		i++;
	}
}
