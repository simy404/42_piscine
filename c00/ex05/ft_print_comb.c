#include <unistd.h>

void	ft_print_comb(void)
{
	char	l;
	char	m;
	char	r;

	l = '0';
	while (l <= '7')
	{
		m = l + 1;
		while (m <= '8')
		{
			r = m + 1;
			while (r <= '9')
			{
				write(1, &l, 1);
				write(1, &m, 1);
				write(1, &r, 1);
				if (l != '7')
					write(1, ", ", 2);
				r++;
			}
			m++;
		}
		l++;
	}
}
